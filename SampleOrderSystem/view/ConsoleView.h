//    - 전체 시료 목록 표 출력(시료 ID, 이름, 평균 생산시간, 수율, 재고 포함)
//    - 이름 등 속성으로 검색한 결과 목록 출력
//
// 4. 시료 주문 화면
//    - 고객명·시료 ID·수량 입력 프롬프트
//    - 생성된 RESERVED 주문 결과 출력
//
// 5. 주문 승인/거절 화면
//    - RESERVED 상태 주문 목록 표 출력
//    - 승인/거절 대상 주문 선택 입력 프롬프트
//    - 승인 결과(CONFIRMED/PRODUCING 분기 결과) 또는 거절 결과(REJECTED) 출력
//
// 6. 모니터링 화면
//    - 상태별 주문 수 집계 출력(REJECTED 제외: RESERVED/PRODUCING/CONFIRMED/RELEASE)
//    - 시료별 재고 현황(여유/부족/고갈) 상태 뱃지 표시
//
// 7. 생산 라인 화면
//    - 현재 생산 중인 시료 정보(실 생산량, 총 생산 시간, 남은 시간 등) 출력
//    - FIFO 대기 큐 목록 출력
//
// 8. 출고 처리 화면
//    - CONFIRMED 상태 주문 목록 표 출력
//    - 출고 대상 주문 선택 입력 프롬프트
//    - RELEASE 처리 결과 출력
//
// 상태 값(RESERVED/REJECTED/PRODUCING/CONFIRMED/RELEASE)과 필드명은
